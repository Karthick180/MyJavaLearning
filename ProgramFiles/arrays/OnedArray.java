package arrays;

public class OnedArray {

	public static void main(String[] args) {
		// One dimensional array
		//int nums[] = new int[4];
		int nums[]= {1,10,100,1000};
		nums[0]= 1;
		nums[1]= 10;
		nums[2]= 100;
		nums[3]= 1000;
		
		for (int i=0;i<4;i++) {
		System.out.println(nums[i]);
		}
	}

}
